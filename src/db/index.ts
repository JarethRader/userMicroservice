// Definition for User document in MongoDB
import { Schema, Model, model } from 'mongoose';

// export interface IUserModel extends IUser, Document {}

export const UserSchema = new Schema({
  username: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
  },
  verified: {
    type: Boolean,
    default: false,
  },
  createdAt: {
    type: Date,
    default: new Date(),
  },
  updatedAt: {
    type: Date,
    default: new Date(),
  },
});

export const User: Model<IUserModel> = model<IUserModel>('User', UserSchema);

export default User;
